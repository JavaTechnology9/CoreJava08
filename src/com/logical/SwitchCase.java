package com.logical;

public class SwitchCase {
	public static void main(String[] args) {
		int month=12;
		switch(month) {
		case 12:
			System.out.println("Month value: "+month);
			break;
		case 5:
			System.out.println("Month value: "+month);
			break;
		case 4:
			System.out.println("Month value: "+month);
			break;
		case 2:
			System.out.println("Month value: "+month);
			break;
		default:
				System.out.println("Default value: "+month);
				
		}
	}

}
